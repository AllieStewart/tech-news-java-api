package com.technews;

public class VariablesPractice {
    public static void main(String[] args) {
        int var1 = 43;
        double var2 = 73.82;
        System.out.println(var1 + var2);
        // comes out as a double, "116.82"

        String someWord = "idk";
        System.out.println(var1 + someWord);
        // comes out as "43idk"
    }
}
